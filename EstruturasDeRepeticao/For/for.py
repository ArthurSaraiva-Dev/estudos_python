print('[range (10) -> 0 a 9 (exclusivo)]')
for item in range(10): # 0 a 9 (exclusivo)
    print(item)

print('[range (2, 9, 2) -> 2 a 8 pulando de 2 em 2 (exclusivo)]')
for item_2 in range(2, 9, 2): # 2 a 8 pulando de 2 em 2 (exclusivo)
    print(item_2)

print('[range (2, 7) -> 2 a 6 (exclusivo)]')
for item_3 in range(2, 7): # 2 a 6 (exclusivo)
    print(item_3)